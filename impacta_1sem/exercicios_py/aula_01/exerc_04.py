print('Olá! digite o valor do lado de um quadrado e iremos te mostrar o valor de sua área!')

lado = float(input('digite o valor do lado de um quadrado:'))
areaQuadrado = (lado)**2
dobroArea = areaQuadrado*2

print('o valor do lado do quadraro é:', lado , 'e sua área:', areaQuadrado, 'm². Também temos o dobro deste valor, sendo:', dobroArea, 'm².')
